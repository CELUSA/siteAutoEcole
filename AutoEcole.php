« Driver » est une auto-école possédant une solide réputation depuis plus de 20 ans. Fort de sa réputation, de nombreux jeunes s’inscrivent pour passer leur permis de conduire. Son principal atout, outre la compétence pédagogique de ses 7 moniteurs, est son offre très diversifiée de permis et tarifs. <br><br>

